import { SignInButton, useUser } from "@clerk/nextjs";
import { type NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import Image from "next/image";

import { api } from "~/utils/api";
import type { RouterOutputs } from "~/utils/api";

import dayjs from "dayjs";
import relativeTime from "dayjs/plugin/relativeTime";
import { LoadingPage } from "~/components/loading";

dayjs.extend(relativeTime);

const CreatePostWizard = () => {
  const { user } = useUser();

  console.log(user);

  if (!user) return null;

  return (
    <div className="flex w-full gap-2">
      <Image
        src={user.profileImageUrl}
        alt="User's Profile Image"
        className=" rounded-full"
        width={56}
        height={56}
      />

      <input
        className="grow bg-inherit outline-none"
        placeholder="Type some emojis!"
      />
    </div>
  );
};

type PostWithUser = RouterOutputs["posts"]["getAll"][number];

const PostView = (props: PostWithUser) => {
  const { post, author } = props;

  return (
    <div key={post.id} className=" flex gap-2 border-b border-slate-400 p-4">
      <Image
        src={author.profileImageUrl}
        alt="Image of the Author"
        className="rounded-full"
        width={56}
        height={56}
      />

      <div className="flex flex-col">
        <div className="flex  gap-1">
          <span>{`@${author.username}`}</span>
          <span className="font-thin">Â·</span>
          <span className="font-thin">{dayjs(post.createdAt).fromNow()}</span>
        </div>

        <div>
          <span>{post.content}</span>
        </div>
      </div>
    </div>
  );
};

const Feed = () => {
  const { data: posts, isLoading: postsLoading } = api.posts.getAll.useQuery();

  if (postsLoading) return <LoadingPage />;

  if (!posts) return <div> Something went wrong... </div>;

  return (
    <div>
      {posts?.map((fullPost) => (
        <PostView {...fullPost} key={fullPost.post.id} />
      ))}
    </div>
  );
};

const Home: NextPage = () => {
  // const hello = api.example.hello.useQuery({ text: "from tRPC" });

  const { isLoaded: userLoaded, isSignedIn } = useUser();

  //Start fetching ASAP --- this is react query
  api.posts.getAll.useQuery();

  if (!userLoaded) {
    return <LoadingPage />;
  }

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex h-screen justify-center">
        <div className=" flex w-full flex-col  border-x border-s-slate-400 md:max-w-2xl">
          <div className="flex border-b border-slate-400 p-4">
            {isSignedIn ? <CreatePostWizard /> : <SignInButton />}
          </div>

          <div className="relative grow">
            <Feed />
          </div>
        </div>
      </main>
    </>
  );
};

export default Home;
